def multiply(a, b):
    return a * b

def subtract(a, b):
    return a - b
def add(a, b):
    """
    Return the sum of two numbers.
    
    Parameters:
    a (int or float): First number
    b (int or float): Second number
    
    Returns:
    int or float: Sum of a and b
    """
    return a + b

def divide(a, b):
    if b == 0:
        return "Error: Division by zero"
    return a / b
